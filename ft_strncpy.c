#include "libft.h"

char *ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t	i;

	i = 0;
	while (src[i] && i < len)
	{
		dst[i] = src[i];
		i++;
	}
	if (i < len && src[i] == '\0')
	{
		while (dst[i])
			dst[i++] = '\0';
	}
    return (dst);
}
